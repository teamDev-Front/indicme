'use client'
import { createContext, useContext, useEffect, useState } from 'react'
import { User as SupabaseUser } from '@supabase/supabase-js'
import { createClient } from '@/utils/supabase/client'
import { useRouter, usePathname } from 'next/navigation'
import toast from 'react-hot-toast'

interface User {
  id: string
  email: string
  full_name: string
  phone: string | null
  role: 'clinic_admin' | 'clinic_viewer' | 'manager' | 'consultant'
  status: 'active' | 'inactive' | 'pending'
  created_at: string
  updated_at: string
}

interface AuthContextType {
  user: SupabaseUser | null
  profile: User | null
  loading: boolean
  signIn: (email: string, password: string) => Promise<void>
  signUp: (email: string, password: string, fullName: string) => Promise<void>
  signOut: () => Promise<void>
  updateProfile: (data: Partial<User>) => Promise<void>
}

const AuthContext = createContext<AuthContextType | undefined>(undefined)

export function AuthProvider({ children }: { children: React.ReactNode }) {
  const [user, setUser] = useState<SupabaseUser | null>(null)
  const [profile, setProfile] = useState<User | null>(null)
  const [loading, setLoading] = useState(true)
  const [initialized, setInitialized] = useState(false)
  const router = useRouter()
  const pathname = usePathname()
  const supabase = createClient()

  // Fun√ß√£o para buscar o perfil do usu√°rio com DEBUG DETALHADO
  const fetchProfile = async (userId: string, retries = 3): Promise<User | null> => {
    try {
      console.log('üîç === INICIANDO BUSCA DE PERFIL ===')
      console.log('üîç User ID:', userId)
      console.log('üîç Tentativas restantes:', retries)
      
      // Primeiro, vamos verificar se a tabela users existe e tem dados
      const { data: tableCheck, error: tableError } = await supabase
        .from('users')
        .select('count')

      console.log('üîç Verifica√ß√£o da tabela users:', { tableCheck, tableError })

      if (tableError) {
        console.error('‚ùå ERRO: Tabela users n√£o encontrada ou sem acesso:', tableError)
        console.error('‚ùå C√≥digo do erro:', tableError.code)
        console.error('‚ùå Mensagem:', tableError.message)
        return null
      }

      // Agora vamos buscar especificamente o usu√°rio
      console.log('üîç Buscando usu√°rio espec√≠fico...')
      const { data, error } = await supabase
        .from('users')
        .select('*')
        .eq('id', userId)
        .single()

      console.log('üîç Resultado da busca:', { data, error })

      if (error) {
        console.error('‚ùå ERRO ao buscar perfil:')
        console.error('‚ùå C√≥digo:', error.code)
        console.error('‚ùå Mensagem:', error.message)
        console.error('‚ùå Detalhes:', error.details)
        console.error('‚ùå Hint:', error.hint)

        // Se o erro √© "not found", vamos tentar criar o perfil
        if (error.code === 'PGRST116') {
          console.log('‚ö†Ô∏è Usu√°rio n√£o encontrado na tabela users, tentando criar...')
          return await createUserProfile(userId)
        }

        if (retries > 0) {
          console.log(`üîÑ Tentando novamente... (${retries} tentativas restantes)`)
          await new Promise(resolve => setTimeout(resolve, 1000))
          return fetchProfile(userId, retries - 1)
        }
        return null
      }

      if (!data) {
        console.log('‚ö†Ô∏è Dados retornados como null/undefined')
        if (retries > 0) {
          console.log(`üîÑ Tentando novamente... (${retries} tentativas restantes)`)
          await new Promise(resolve => setTimeout(resolve, 1000))
          return fetchProfile(userId, retries - 1)
        }
        return null
      }

      console.log('‚úÖ Perfil encontrado com sucesso!')
      console.log('‚úÖ Dados do perfil:', JSON.stringify(data, null, 2))
      return data
    } catch (error) {
      console.error('‚ùå CATCH: Erro na busca do perfil:', error)
      if (retries > 0) {
        await new Promise(resolve => setTimeout(resolve, 1000))
        return fetchProfile(userId, retries - 1)
      }
      return null
    }
  }

  // Fun√ß√£o para criar perfil se n√£o existir
  const createUserProfile = async (userId: string): Promise<User | null> => {
    try {
      console.log('üèóÔ∏è Tentando criar perfil para usu√°rio:', userId)

      // Buscar dados do usu√°rio auth
      const { data: { user: authUser }, error: authError } = await supabase.auth.getUser()
      
      if (authError || !authUser) {
        console.error('‚ùå Erro ao buscar dados do usu√°rio auth:', authError)
        return null
      }

      console.log('üèóÔ∏è Dados do usu√°rio auth:', {
        id: authUser.id,
        email: authUser.email,
        user_metadata: authUser.user_metadata,
        app_metadata: authUser.app_metadata
      })

      // Criar perfil b√°sico
      const newProfile = {
        id: userId,
        email: authUser.email!,
        full_name: authUser.user_metadata?.full_name || authUser.email?.split('@')[0] || 'Usu√°rio',
        phone: authUser.phone || null,
        role: 'consultant' as const,
        status: 'active' as const,
      }

      console.log('üèóÔ∏è Criando perfil com dados:', newProfile)

      const { data, error } = await supabase
        .from('users')
        .insert(newProfile)
        .select()
        .single()

      if (error) {
        console.error('‚ùå Erro ao criar perfil:', error)
        return null
      }

      console.log('‚úÖ Perfil criado com sucesso:', data)
      return data
    } catch (error) {
      console.error('‚ùå Erro na cria√ß√£o do perfil:', error)
      return null
    }
  }

  // Fun√ß√£o para verificar o usu√°rio atual
  const checkUser = async () => {
    try {
      console.log('üîç === INICIANDO VERIFICA√á√ÉO DE USU√ÅRIO ===')
      
      // Usar getUser() que √© mais seguro e faz verifica√ß√£o no servidor
      const { data: { user }, error } = await supabase.auth.getUser()
      
      console.log('üîç Resultado getUser:', { user: user?.id, error })

      if (error) {
        console.error('‚ùå Erro ao verificar usu√°rio:', error)
        setUser(null)
        setProfile(null)
        return
      }

      console.log('üë§ Usu√°rio encontrado:', user?.id)
      setUser(user)
      
      if (user) {
        console.log('üîç Iniciando busca do perfil...')
        const userProfile = await fetchProfile(user.id)
        console.log('üîç Perfil retornado:', userProfile ? 'ENCONTRADO' : 'N√ÉO ENCONTRADO')
        setProfile(userProfile)
      } else {
        console.log('‚ùå Nenhum usu√°rio encontrado')
        setProfile(null)
      }
    } catch (error) {
      console.error('‚ùå CATCH: Erro na verifica√ß√£o do usu√°rio:', error)
      setUser(null)
      setProfile(null)
    }
  }

  // Inicializa√ß√£o do auth
  useEffect(() => {
    if (initialized) return

    console.log('üöÄ === INICIALIZANDO AUTH CONTEXT ===')
    setInitialized(true)

    const initAuth = async () => {
      await checkUser()
      console.log('üèÅ Finalizando loading inicial')
      setLoading(false)
    }

    initAuth()

    // Listener para mudan√ßas de autentica√ß√£o
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        console.log('üîÑ === AUTH STATE CHANGED ===')
        console.log('üîÑ Event:', event)
        console.log('üîÑ Session user ID:', session?.user?.id)
        
        try {
          setUser(session?.user ?? null)
          
          if (session?.user) {
            console.log('üîÑ Buscando perfil ap√≥s mudan√ßa de estado...')
            const userProfile = await fetchProfile(session.user.id)
            console.log('üîÑ Perfil ap√≥s mudan√ßa:', userProfile ? 'ENCONTRADO' : 'N√ÉO ENCONTRADO')
            setProfile(userProfile)
          } else {
            console.log('üîÑ Limpando perfil (sem usu√°rio)')
            setProfile(null)
          }
        } catch (error) {
          console.error('‚ùå Erro na mudan√ßa de estado de auth:', error)
          setUser(null)
          setProfile(null)
        }
        
        console.log('üîÑ Finalizando loading ap√≥s mudan√ßa de estado')
        setLoading(false)
      }
    )

    return () => {
      console.log('üßπ Limpando subscription do auth')
      subscription.unsubscribe()
    }
  }, [initialized])

  const signIn = async (email: string, password: string) => {
    try {
      setLoading(true)
      console.log('üîë === INICIANDO LOGIN ===')
      console.log('üîë Email:', email)
      
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password,
      })

      if (error) {
        console.error('‚ùå Erro no login:', error)
        throw error
      }

      if (data.user) {
        console.log('‚úÖ Login realizado com sucesso!')
        console.log('‚úÖ User ID:', data.user.id)
        toast.success('Login realizado com sucesso!')
        
        // Aguardar um pouco para garantir que o estado foi atualizado
        await new Promise(resolve => setTimeout(resolve, 500))
        
        // S√≥ redirecionar se n√£o estiver em uma p√°gina de auth
        if (pathname?.startsWith('/auth')) {
          router.push('/dashboard')
        }
      }
    } catch (error: any) {
      console.error('‚ùå Erro no login:', error)
      toast.error(error.message || 'Erro ao fazer login')
      throw error
    } finally {
      setLoading(false)
    }
  }

  const signUp = async (email: string, password: string, fullName: string) => {
    try {
      setLoading(true)
      console.log('üìù === INICIANDO CADASTRO ===')
      console.log('üìù Email:', email)
      console.log('üìù Nome:', fullName)
      
      const { data, error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          data: {
            full_name: fullName,
          },
        },
      })

      if (error) {
        console.error('‚ùå Erro no cadastro:', error)
        throw error
      }

      if (data.user) {
        console.log('‚úÖ Conta criada com sucesso!')
        console.log('‚úÖ User ID:', data.user.id)
        
        toast.success('Conta criada com sucesso! Verifique seu email.')
      }
    } catch (error: any) {
      console.error('‚ùå Erro no cadastro:', error)
      toast.error(error.message || 'Erro ao criar conta')
      throw error
    } finally {
      setLoading(false)
    }
  }

  const signOut = async () => {
    try {
      setLoading(true)
      console.log('üö™ === INICIANDO LOGOUT ===')
      
      const { error } = await supabase.auth.signOut()
      
      if (error) {
        console.error('‚ùå Erro no logout:', error)
        throw error
      }

      console.log('‚úÖ Logout realizado com sucesso!')
      
      // Limpar estados imediatamente
      setUser(null)
      setProfile(null)
      
      toast.success('Logout realizado com sucesso!')
      
      // Aguardar um pouco antes de redirecionar
      await new Promise(resolve => setTimeout(resolve, 300))
      
      // Force reload para limpar qualquer cache
      window.location.href = '/auth/login'
    } catch (error: any) {
      console.error('‚ùå Erro ao fazer logout:', error)
      toast.error(error.message || 'Erro ao fazer logout')
      setLoading(false)
      throw error
    }
  }

  const updateProfile = async (data: Partial<User>) => {
    try {
      if (!user) throw new Error('Usu√°rio n√£o encontrado')

      console.log('üìù === ATUALIZANDO PERFIL ===')
      console.log('üìù Dados:', data)

      const { error } = await supabase
        .from('users')
        .update({
          ...data,
          updated_at: new Date().toISOString()
        })
        .eq('id', user.id)

      if (error) {
        throw error
      }

      // Recarregar o perfil
      const updatedProfile = await fetchProfile(user.id)
      if (updatedProfile) {
        setProfile(updatedProfile)
      }
      
      console.log('‚úÖ Perfil atualizado com sucesso!')
      toast.success('Perfil atualizado com sucesso!')
    } catch (error: any) {
      console.error('‚ùå Erro ao atualizar perfil:', error)
      toast.error(error.message || 'Erro ao atualizar perfil')
      throw error
    }
  }

  // Debug do estado atual
  useEffect(() => {
    console.log('üìä === ESTADO ATUAL ===')
    console.log('üìä Loading:', loading)
    console.log('üìä User:', user?.id)
    console.log('üìä Profile:', profile?.id)
    console.log('üìä Initialized:', initialized)
  }, [loading, user, profile, initialized])

  const value = {
    user,
    profile,
    loading,
    signIn,
    signUp,
    signOut,
    updateProfile,
  }

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  )
}

export function useAuth() {
  const context = useContext(AuthContext)
  if (context === undefined) {
    throw new Error('useAuth deve ser usado dentro de um AuthProvider')
  }
  return context
}